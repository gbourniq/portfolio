def redit(string) -> str:
    return f"\u001b[31;1m{string}\u001b[0m"


def greenit(string) -> str:
    return f"\u001b[32;1m{string}\u001b[0m"


def blueit(string) -> str:
    return f"\u001b[34;1m{string}\u001b[0m"


def whiteit(string) -> str:
    return f"\u001b[1m{string}\u001b[0m"


def bolderlineit(string) -> str:
    return f"\u001b[1m\u001b[4m{string}\u001b[0m"
